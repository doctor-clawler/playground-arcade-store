export const CURATED_PLAYER_WORDS = Object.freeze([...new Set([...EASY_KOREAN_WORDS, ...PLAYER_EXTRA_WORDS])]);
export const PLAYER_DICTIONARY_WORDS = Object.freeze([...new Set([...CURATED_PLAYER_WORDS, ...GENERATED_KOREAN_WORDS])]);

const WORD_INDEX = new Map(EASY_KOREAN_WORDS.map((word, index) => [word, index]));
const PLAYER_WORD_INDEX = new Map(PLAYER_DICTIONARY_WORDS.map((word, index) => [word, index]));
const CURATED_WORD_SET = new Set(CURATED_PLAYER_WORDS);
const WORD_SET = new Set(PLAYER_DICTIONARY_WORDS);
const AI_WORDS_BY_FIRST_SYLLABLE = groupWordsByFirstSyllable(EASY_KOREAN_WORDS);
const PLAYER_WORDS_BY_FIRST_SYLLABLE = groupWordsByFirstSyllable(PLAYER_DICTIONARY_WORDS);
const CURATED_WORDS_BY_FIRST_SYLLABLE = groupWordsByFirstSyllable(CURATED_PLAYER_WORDS);
const HANGUL_WORD_PATTERN = /^[가-힣]+$/;
const WIN_REWARD = 2000;

function groupWordsByFirstSyllable(words) {
  const groups = new Map();
  for (const word of words) {
    const syllable = firstSyllable(word);
    if (!syllable) {
      continue;
    }
    const group = groups.get(syllable);
    if (group) {
      group.push(word);
    } else {
      groups.set(syllable, [word]);
    }
  }
  return groups;
}

export function normalizeWord(value) {
  return String(value ?? "").replace(/\s+/g, "").trim();
}

export function firstSyllable(value) {
  const word = normalizeWord(value);
  return word ? word[0] : "";
}

export function lastSyllable(value) {
  const word = normalizeWord(value);
  return word ? word[word.length - 1] : "";
}

export function syllableWithRo(value) {
  const syllable = firstSyllable(value);
  if (!syllable) {
    return "";
  }
  const code = syllable.charCodeAt(0);
  const hangulStart = 0xac00;
  const hangulEnd = 0xd7a3;
  if (code < hangulStart || code > hangulEnd) {
    return `${syllable}로`;
  }
  const finalConsonantIndex = (code - hangulStart) % 28;
  const hasFinalConsonant = finalConsonantIndex !== 0 && finalConsonantIndex !== 8;
  return `${syllable}${hasFinalConsonant ? "으로" : "로"}`;
}

export function isDictionaryWord(value) {
  return WORD_SET.has(normalizeWord(value));
}

export function createGame(options = {}) {
  return {
    mode: options.mode ?? "playing",
    turn: options.turn ?? "player",
    balance: options.balance ?? 0,
    currentSyllable: options.currentSyllable ?? null,
    usedWords: [...(options.usedWords ?? [])].map(normalizeWord),
    history: [...(options.history ?? [])],
    message: options.message ?? "먼저 단어를 말해 주세요.",
    lastResult: options.lastResult ?? null,
  };
}

export function createLobby(balance = 0, lastResult = null) {
  return {
    mode: "lobby",
    turn: null,
    balance,
    currentSyllable: null,
    usedWords: [],
    history: [],
    message: lastResult?.message ?? "플레이 버튼을 눌러 시작하세요.",
    lastResult,
  };
}

export function getAvailableWords(state, syllable, words = PLAYER_DICTIONARY_WORDS) {
  const used = new Set(state.usedWords ?? []);
  const candidates = getWordsForSyllable(syllable, words);
  return candidates.filter((word) => !used.has(word));
}

function getWordsForSyllable(syllable, words) {
  if (!syllable) {
    return [];
  }
  if (words === PLAYER_DICTIONARY_WORDS) {
    return PLAYER_WORDS_BY_FIRST_SYLLABLE.get(syllable) ?? [];
  }
  if (words === EASY_KOREAN_WORDS) {
    return AI_WORDS_BY_FIRST_SYLLABLE.get(syllable) ?? [];
  }
  if (words === CURATED_PLAYER_WORDS) {
    return CURATED_WORDS_BY_FIRST_SYLLABLE.get(syllable) ?? [];
  }
  return words.filter((word) => firstSyllable(word) === syllable);
}

export function chooseAiWord(state, syllable) {
  const words = getAvailableWords(state, syllable, EASY_KOREAN_WORDS);
  if (!words.length) {
    return null;
  }
  const sortedWords = words
    .slice()
    .sort((left, right) => left.length - right.length || WORD_INDEX.get(left) - WORD_INDEX.get(right));

  return (
    sortedWords.find((word) => {
      const nextState = {
        ...state,
        usedWords: [...(state.usedWords ?? []), word],
      };
      return choosePlayerHintWord(nextState, lastSyllable(word), CURATED_PLAYER_WORDS);
    }) ?? null
  );
}

export function choosePlayerHintWord(state, syllable, words = PLAYER_DICTIONARY_WORDS) {
  const availableWords = getAvailableWords(state, syllable, words);
  if (!availableWords.length) {
    return null;
  }
  return availableWords
    .slice()
    .sort((left, right) => {
      const curatedDelta = Number(!CURATED_WORD_SET.has(left)) - Number(!CURATED_WORD_SET.has(right));
      const leftRank = PLAYER_WORD_INDEX.get(left) ?? Number.MAX_SAFE_INTEGER;
      const rightRank = PLAYER_WORD_INDEX.get(right) ?? Number.MAX_SAFE_INTEGER;
      return curatedDelta || left.length - right.length || leftRank - rightRank;
    })[0];
}

function finishGame(state, winner, reason) {
  const playerWon = winner === "player";
  const reward = playerWon ? WIN_REWARD : 0;
  const message = playerWon ? "이겼어요! 2,000원을 받았어요." : "졌어요. 로비로 돌아왔어요.";
  return {
    ...state,
    mode: "lobby",
    turn: null,
    balance: state.balance + reward,
    currentSyllable: null,
    message,
    lastResult: {
      winner,
      reward,
      reason,
      message,
    },
  };
}

function rejectMove(state, message) {
  return {
    ...state,
    message,
  };
}

function validatePlayerWord(state, word) {
  if (state.mode !== "playing") {
    return "플레이 버튼을 눌러 다시 시작하세요.";
  }
  if (!word) {
    return "단어를 입력해 주세요.";
  }
  if (!HANGUL_WORD_PATTERN.test(word)) {
    return "한글 단어만 쓸 수 있어요.";
  }
  if (!isDictionaryWord(word)) {
    return "사전에 있는 단어만 쓸 수 있어요.";
  }
  if ((state.usedWords ?? []).includes(word)) {
    return "이미 쓴 단어예요.";
  }
  if (state.currentSyllable && firstSyllable(word) !== state.currentSyllable) {
    return `${syllableWithRo(state.currentSyllable)} 시작하는 단어를 말해 주세요.`;
  }
  return null;
}

export function submitPlayerWord(state, rawWord) {
  const word = normalizeWord(rawWord);
  const validationMessage = validatePlayerWord(state, word);
  if (validationMessage) {
    return rejectMove(state, validationMessage);
  }

  const afterPlayer = {
    ...state,
    usedWords: [...state.usedWords, word],
    history: [...state.history, { speaker: "player", word }],
  };
  const aiStart = lastSyllable(word);
  const aiWord = chooseAiWord(afterPlayer, aiStart);

  if (!aiWord) {
    return finishGame(afterPlayer, "player", `AI가 ${syllableWithRo(aiStart)} 시작하는 단어를 찾지 못했어요.`);
  }

  const afterAiBase = {
    ...afterPlayer,
    turn: "player",
    usedWords: [...afterPlayer.usedWords, aiWord],
    history: [...afterPlayer.history, { speaker: "ai", word: aiWord }],
    currentSyllable: lastSyllable(aiWord),
  };
  const playerHintWord = choosePlayerHintWord(afterAiBase, afterAiBase.currentSyllable);

  if (!playerHintWord) {
    return finishGame(afterAiBase, "ai", `${syllableWithRo(afterAiBase.currentSyllable)} 시작하는 단어가 없어요.`);
  }

  return {
    ...afterAiBase,
    message: `AI: ${aiWord}. ${syllableWithRo(afterAiBase.currentSyllable)} 시작해 주세요. 예: ${playerHintWord}`,
  };
}

export function giveUp(state) {
  if (state.mode !== "playing") {
    return rejectMove(state, "플레이 버튼을 눌러 다시 시작하세요.");
  }
  if (!state.currentSyllable) {
    return rejectMove(state, "먼저 단어를 말해 주세요.");
  }
  if (getAvailableWords(state, state.currentSyllable).length) {
    return rejectMove(state, "아직 이어갈 수 있는 단어가 있어요.");
  }
  return finishGame(state, "ai", `${syllableWithRo(state.currentSyllable)} 시작하는 단어가 없어요.`);
}
